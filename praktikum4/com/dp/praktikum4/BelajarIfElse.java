package com.dp.praktikum4;

public class BelajarIfElse {
    public static void main(String[] args) {
        int nilai=80;
        if (nilai>=90) {
            System.out.println("A");
        }
        else if (nilai>=70) {
            System.out.println("B");
        }
        else if (nilai>=60) {
            System.out.println("C");
        }
        else {
            System.out.println("Tidak Lulus");
        }
    }
}
